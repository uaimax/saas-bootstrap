export const SocialButton: React.FC<SocialButtonProps> = ({
  provider,
  name,
  disabled,
  className,
}) => {
  const handleClick = () => {
    const companySlug = localStorage.getItem('company_id') || localStorage.getItem('tenant_id');
    initiateSocialLogin(provider, companySlug || undefined);
  };

  return (
    <Button
      variant="outline"
      type="button"
      disabled={disabled}
      onClick={handleClick}
      className={cn("w-full", className)}
    >
      {getProviderIcon(provider)}
      <span>Continuar com {name}</span>
    </Button>
  );
};

